<h1>History</h1>
<p>&nbsp;</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Computer System Administrator Group หรือ CSAG จัดตั้งขึ้นเมื่อปี พ.ศ.2549 โดยการรวมกลุ่มกันของนักศึกษาที่สนใจทางด้าน System, Network และความร่วมมือจากสำนักบริการคอมพิวเตอร์
    ซึ่งในขณะนั้นยังเป็นสำนักวิจัยและบริการคอมพิวเตอร์อยู่ หลังจากก่อตั้งก็ได้ผลิตและพัฒนาผลงาน ทางด้านเทคโนโลยีสารสนเทศเพื่อให้บริการแก่สถาบัน และได้มีการรับสมัครนักศึกษาที่สนใจ ตั้งแต่นั้นเป็นต้นมา
</p>
